<template>
  <div :class="['message', message.role, { 'streaming': isStreaming }]">
    <div class="message-header">
      <div class="message-avatar">
        {{ message.role === "user" ? "👤" : "🤖" }}
      </div>
      <div class="message-role">
        {{ message.role === "user" ? "User" : "AI" }}
        <button
          v-if="isStreaming"
          :class="['stop-btn', { 'streaming': isStreaming }]"
          @click="$emit('stop-streaming')"
          title="停止生成"
        >
          ⏹️
        </button>
      </div>
    </div>
    <div class="message-content">
      <div
        class="message-text"
        v-if="message.role === 'user'"
        v-html="formatMessage(message.content)"
      ></div>
      <div
        v-else-if="message.role === 'assistant'"
        class="message-text"
        v-html="parseMarkdown(message.content)"
      ></div>
      <div
        v-else-if="isStreaming && !message.content"
        class="message-text typing-indicator"
      >
        <span></span>
        <span></span>
        <span></span>
      </div>
      <div class="message-time">{{ formatTime(message.timestamp) }}</div>
    </div>
  </div>
</template>

<script lang="ts" setup>
import { marked } from "marked";

interface ChatMessage {
  role: "user" | "assistant" | "system";
  content: string;
  timestamp: Date;
  isStreaming?: boolean; // 标记消息是否正在流式传输中
}

const props = defineProps<{
  message: ChatMessage;
  isStreaming?: boolean;
}>();

const emit = defineEmits<{
  "stop-streaming": [];
}>();

// 简单的Markdown格式化
const formatMessage = (content: string): string => {
  // 简单的Markdown格式化
  return content
    .replace(/\*\*(.*?)\*\*/g, "<strong>$1</strong>")
    .replace(/\*(.*?)\*/g, "<em>$1</em>")
    .replace(/`(.*?)`/g, "<code>$1</code>")
    .replace(/\n/g, "<br>");
};

// 解析 Markdown 内容
const parseMarkdown = (content: string): string => {
  try {
    // 使用 marked 的同步解析方式，参考 AISummaryPanel.vue
    return content ? (marked.parse(content, { async: false }) as string) : "";
  } catch (error) {
    console.error("Markdown parsing error:", error);
    return content;
  }
};

// 调试日志：组件挂载和更新时的消息状态
console.log("MessageItem: 渲染消息", {
  role: props.message.role,
  contentLength: props.message.content?.length || 0,
  isStreaming: props.isStreaming,
  timestamp: props.message.timestamp
});

const formatTime = (timestamp: Date): string => {
  return new Intl.DateTimeFormat("zh-CN", {
    hour: "2-digit",
    minute: "2-digit",
  }).format(timestamp);
};
</script>

